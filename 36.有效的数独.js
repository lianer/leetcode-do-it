/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function (board) {
  const rows = new Array(9).fill(0).map(() => new Array(9).fill(0));
  const columns = new Array(9).fill(0).map(() => new Array(9).fill(0));
  const subboards = new Array(3).fill(0).map(() => new Array(3).fill(0).map(() => new Array(9).fill(0)));
  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      const c = board[i][j];
      if (c !== '.') {
        const code = c.charCodeAt() - '0'.charCodeAt() - 1;
        rows[i][code]++;
        columns[j][code]++;
        subboards[Math.floor(i / 3)][Math.floor(j / 3)][code]++;
        if (rows[i][code] > 1 || columns[j][code] > 1 || subboards[Math.floor(i / 3)][Math.floor(j / 3)][code] > 1) {
          return false;
        }
      }
    }
  }
  return true;
};
